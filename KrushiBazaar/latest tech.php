<p><b>Increase in production of food grain</b></p>
<p>
Due to the Green revolution in India, the production of food has significantly increased. This revolution began in 1967 under the leadership of Congressman Shri Lal Bahadur Shastri. The aim was to increase food production in mainly three states in India, i.e. Punjab, Haryana, and Uttar Pradesh. The state of Punjab excelled in the implementation of the idea and is often called the bread basket of India.

And it was successfully achieved as new DNA-modified varieties of food grains was introduced. Thus, this trend has left its impact in terms of better-yielding varieties of food grains, drought-resistant varieties of crops, etc. This was and is the latest trend in agriculture in India. On a yearly basis, due to research and development, new crop varieties are introduce.</p>
    <p><b>Diversification in agriculture</b></p>
    <p>
The diversification in agriculture was a much-needed change to improve soil fertility and quality while there was a rush for crop production. Diversification of agriculture usually consists of the growth of horticultural crops, vegetables, oils, nitrogen-fixation plants, etc. This is one of the latest trends in agriculture in India. This is a much-needed change as this latest technology trend also needs a label.</p>

<p><b>Horticulture and its output in the present scenarios</b></p>
<p>Due to India’s wide variety of soil conditions and textures, horticulture is constantly growing. According to reports, India is the second largest producer of vegetable crops and the largest producer of fruits in the world. This latest trend in agriculture in India is growing with the flow. Therefore constituting a large part of India’s GDP.</p>

<p><b>Floriculture and its increasing effect on the Indian economy</b></p>
<p>Floriculture is one of the latest trends in agriculture in India, with a contribution of INR 266 Billion. As the technology relating to agriculture is improving day by day, flower production, a.k.a. floriculture, is improving day by day. As per reports, in floriculture around 31000 hectares of land in southern states of India involved.</p>

